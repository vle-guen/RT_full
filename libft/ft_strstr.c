#include "libft.h"

char	*ft_strstr(const char *s1, const char *s2)
{
	int i;
	int i2;
	int it;

	i2 = 0;
	i = 0;
	if (s2[0] == '\0')
		return ((void *)&s1[0]);
	while (s1[i] != '\0')
	{
		if (s1[i] == s2[0])
		{
			it = i;
			while ((s1[it] == s2[i2]) && s2[i2] != '\0' && s1[it] != '\0')
			{
				i2++;
				it++;
			}
			if (s2[i2] == '\0')
				return ((void *)&s1[i]);
			i2 = 0;
		}
		i++;
	}
	return (NULL);
}
